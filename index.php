    });

    // Mobile nav toggle
    function toggleNav() {
        document.getElementById('navLinks').classList.toggle('nav-open');
        document.getElementById('hamburger').classList.toggle('active');
    }

    // Smooth scroll
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function(e) {
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                e.preventDefault();
                window.scrollY > 0 && document.getElementById('navLinks').classList.remove('nav-open');
                target.scrollIntoView({ behavior: 'smooth', block: 'start' });
            }
        });
    });

    // Scroll reveal
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('revealed');
            }
        });
    }, { threshold: 0.1 });

    document.querySelectorAll('.about-card, .product-card, .advantage-item, .step-card, .testi-card, .kontak-info, .kontak-form').forEach(el => {
        el.classList.add('reveal-anim');
        observer.observe(el);
    });

    // Contact form
    function submitKontak(e) {
        e.preventDefault();
        document.getElementById('kontakSuccess').style.display = 'block';
        e.target.reset();
    }

    // Animate hero stats numbers
    function animateCounter(el, target) {
        let current = 0;
        const step = target / 60;
        const timer = setInterval(() => {
            current = Math.min(current + step, target);
            el.textContent = Math.floor(current).toLocaleString('id-ID') + (el.dataset.suffix || '');
            if (current >= target) clearInterval(timer);
        }, 16);
    }
